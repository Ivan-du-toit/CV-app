<h2>Why did I create this.</h2>
<p>I created this CV because I always thought that the traditional CV is out dated in the web age. But since some companies don't yet embrace all of the web age I still needed it to resemble a normal CV so it could be printed and read like a normal CV.</p>
<p>I also felt it very limiting to only include what I though was important in my CV while the document is to be read by someone else who is really the person who should decide. For this reason I thought that one should create a CV like the web; store inordinate amounts of information but make it accessible with good search and transversal tools.</p>

